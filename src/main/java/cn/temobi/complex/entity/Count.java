package cn.temobi.complex.entity;

public class Count {
	private Long zCount;
	private Long pCount;
	private Long isZCount;
	private Long isPCount;
	private Long productCount;
	private String date;
	public Long getzCount() {
		return zCount;
	}
	public void setzCount(Long zCount) {
		this.zCount = zCount;
	}
	public Long getpCount() {
		return pCount;
	}
	public void setpCount(Long pCount) {
		this.pCount = pCount;
	}
	public Long getIsZCount() {
		return isZCount;
	}
	public void setIsZCount(Long isZCount) {
		this.isZCount = isZCount;
	}
	public Long getIsPCount() {
		return isPCount;
	}
	public void setIsPCount(Long isPCount) {
		this.isPCount = isPCount;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public Long getProductCount() {
		return productCount;
	}
	public void setProductCount(Long productCount) {
		this.productCount = productCount;
	}

	
}
